'use strict';

const sum = (a, b) => {
    return a + b;
};
const deduction = (a, b) => {
    return a - b;
};
const multipl = (a, b) => {
    return a * b;
};
const div = (a, b) => {
    return a / b;
};

console.log(sum(6, 2));
console.log(deduction(6, 2));
console.log(multipl(6, 2));
console.log(div(6, 2));

